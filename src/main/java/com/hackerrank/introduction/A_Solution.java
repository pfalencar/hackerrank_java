package com.hackerrank.introduction;

import java.util.*;

public class A_Solution {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        System.out.print("Enter with first number: ");
        int a = scan.nextInt();
        System.out.print("Enter with second number: ");
        int b = scan.nextInt();
        System.out.print("Enter with third number: ");
        int c = scan.nextInt();
        scan.close();

        System.out.println("First number is: " + a);
        System.out.println("Second number is: " + b);
        System.out.println("Third number is: " + c);
    }
}
